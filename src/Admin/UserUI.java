    private void checkAndCreateAdminFile() {
        File adminFile = new File("Admin.txt");
        if (!adminFile.exists()) {
            // File doesn't exist, prompt for new admin password
            String newPassword = JOptionPane.showInputDialog(this, "Enter new admin password:");
            if (newPassword != null && !newPassword.isEmpty()) {
                try (FileWriter writer = new FileWriter(adminFile)) {
                    writer.write(newPassword);
                } catch (IOException e) {
                    JOptionPane.showMessageDialog(this, "Error writing to Admin.txt: " + e.getMessage());
                }
            }
        }
    }
    
    private String readAdminPassword() {
    File adminFile = new File("Admin.txt");
    if (adminFile.exists()) {
        try (Scanner scanner = new Scanner(adminFile)) {
            return scanner.nextLine();
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error reading from Admin.txt: " + e.getMessage());
        }
    }
    return null; // Return null if file does not exist or any error occurs
}

