    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String adminPassword = readAdminPassword();

    if (adminPassword != null) {
        String inputPassword = JOptionPane.showInputDialog(this, "Enter admin password:");

        if (inputPassword != null && inputPassword.equals(adminPassword)) {
            // Correct password, proceed with admin actions
            // For example, open the admin UI or perform admin tasks
            AdminUI adminUI = new AdminUI();
            adminUI.setVisible(true);
            this.dispose();
        } else {
            // Incorrect password, show error message
            JOptionPane.showMessageDialog(null, "Incorrect password.");
        }
    } else {
        // Admin password not set or file not found, handle accordingly
        JOptionPane.showMessageDialog(null, "Admin password not found. Please set up an admin password first.");
    }
    }//GEN-LAST:event_jButton4ActionPerformed

