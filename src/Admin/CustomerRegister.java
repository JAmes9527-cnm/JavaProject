    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        String topupinput = jTextField1.getText();
        String id = tfID.getText();
        double credit = Double.parseDouble(tfCredit.getText());
        double afterAmount = 0;
        
        if(row==-1 || id.isEmpty()){
            JOptionPane.showMessageDialog(this,"Please select a row");
        }
        else {
    try {
        double topUpAmount = Double.parseDouble(topupinput);
        // Process the topUpAmount (since parsing was successful)
        Customer.topupCustomerCredit(id,topUpAmount);
        afterAmount = credit +topUpAmount;
        Receipt receipt = new Receipt(id, credit, afterAmount);
        Transaction tran = new Transaction(Transaction.type.TOPUP, credit, afterAmount);
        tran.addTransactionRecord(id);
        String messageText = receipt.toString();
        Message ms = new Message(id,messageText);
        Message.addMessage(ms);
        
        String name = tfName.getText();
        String password = tfPassword.getText();
        
        String[] values = {id, name, password,String.valueOf(afterAmount)};
        model.removeRow(row);
        model.insertRow(row,values);
        Cleartf();
        
        JOptionPane.showMessageDialog(this, "Top up success.");
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Invalid input. Please enter a valid double value.");
    
    }
        }
    }//GEN-LAST:event_jButton5ActionPerformed

