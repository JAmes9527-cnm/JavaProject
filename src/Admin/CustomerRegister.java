    public void vendortable(){       
        File file = new File("Customer.txt");
        if (file.exists()) {
        try (BufferedReader reader = new BufferedReader(new FileReader("Customer.txt"))) {
        String line;
        //boolean isFirstLine = true;

        while ((line = reader.readLine()) != null) {
           

            String[] MenuData = line.split(",");
            if (MenuData.length == 4) {
                String Id = MenuData[0];
                String Name = MenuData[1];
                String Password = MenuData[2];
                String Credit = MenuData[3];
               
                model.addRow(new Object[]{Id,Name,Password,Credit});
            } else {
                System.out.println("Invalid line: " + line);
            }
        }
        
        }   
        catch (IOException ex) {
            Logger.getLogger(customerMenu.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    }

